package Latihan02.Praktikum10.inheritance;

public class Operasi
{
public Integer opTambah(int a)
{
return a++;
}

public Integer opTambah2(int a)
{
return ++a;
}

public Integer opKurang(int a)
{
return a--;
}

public Integer opKurang2(int a)
{
return --a;
}

public boolean apakahLebihBesar(int a, int b)
{
return a > b;
}

public boolean apakahLebihKecil(int a, int b)
{
return a < b;
}

public boolean apakahSamaDengan(int a, int b)
{
return a == b;
}
}

